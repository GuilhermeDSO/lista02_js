let diaDaSemana =prompt("Digite um número que represente o dia da semana")

if(diaDaSemana ==1){
    alert("Domingo")
}else 
    if (diaDaSemana == 2){
        alert("Segunda-feira")
    } 
if(diaDaSemana ==3){
    alert("Terça-feira")
}else 
    if (diaDaSemana == 4){
        alert("Quarta-feira")
    } 
if(diaDaSemana ==5){
    alert("Quinta-feira")
}else 
    if (diaDaSemana == 6){
        alert("Sexta-feira")
    } 
if(diaDaSemana ==7){
    alert("Sábado")
}else{
    alert("Não ha dia correspondente")
}